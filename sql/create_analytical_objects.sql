CREATE OR REPLACE STREAM PAYMENT_STREAM ON TABLE FACT_PAYMENTS;

-- Task to run Daily Data Refresh at 1:00 AM UTC
CREATE OR REPLACE TASK DAILY_DATA_REFRESH_TASK
    WAREHOUSE = ECOMMERCE_WH
    SCHEDULE = 'USING CRON 0 1 * * * UTC'
AS
    CALL REFRESH_ALL_ANALYTICS();

-- Resume the task to start the schedule
ALTER TASK DAILY_DATA_REFRESH_TASK RESUME;


